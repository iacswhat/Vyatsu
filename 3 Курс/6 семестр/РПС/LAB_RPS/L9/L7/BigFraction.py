

# Умножение
multiply_num = lambda main_num, sec_num: main_num * sec_num

multiply_den = lambda main_den, sec_den: main_den * sec_den

# Деление
divide_num = lambda main_num, sec_den: main_num * sec_den

divide_den = lambda main_den, sec_num: main_den * sec_num

# Сложение
sum_num = lambda main_num, sec_num: main_num + sec_num

# Вычитание
sub_num = lambda main_num, sec_num: main_num - sec_num

# Общее
num_main = lambda main_num, sec_den: main_num * sec_den
num_sec = lambda main_den, sec_num: sec_num * main_den
